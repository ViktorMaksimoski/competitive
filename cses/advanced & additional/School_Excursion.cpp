template <class T>
struct DSU {
    vector<T> parent;
    vector<int> size;
    int components;

    DSU(int n) {
        parent.resize(n+1);
        for(int i=0; i<n+1; i++) parent[i] = i;
        size.resize(n+1, 1);
        components = n;
    }

    T find(T x) {
        if(parent[x] == x) return x;
        return parent[x] = find(parent[x]);
    }

    T getSize(T x) {
        return size[find(x)];
    }

    bool uni(T a, T b) {
        T leaderA = find(a), leaderB = find(b);

        if(leaderA == leaderB) {
            return false;
        }

        components--;

        if(size[leaderA] > size[leaderB]) swap(leaderA, leaderB);

        parent[leaderA] = leaderB;
        size[leaderB] += size[leaderA];
        return true;
    }

    int getComponents() {
        return components;
    }

    bool same_set(T a, T b) {
        return find(a) == find(b);
    }
};

int32_t main() {
    setIO();

    int n, m;
    cin >> n >> m;
    DSU<int> dsu(n);

    for(int i=0; i<m; i++) {
        int a, b;
        cin >> a >> b;
        dsu.uni(a, b);
    }

    vector<int> v;
    for(int i=1; i<=n; i++)
        if(dsu.find(i) == i)
            v.pb(dsu.getSize(i));

    bitset<100001> bs;
    bs[1] = 1;

    for(int &x : v)
        bs |= (bs << x);

    for(int i=n; i>=1; i--)
        cout << bs[i];
    return 0;
}